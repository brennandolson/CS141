# Write a program which accepts a list of words from the command
# line and outputs any pairs of words which are reverses.
# (a palindrome doesn't count as its own reverse unless
# it appears twice in the list).

# An example run of this program might look like this:
# 
# python3 Lab12.py a edam pools racecar part made sloop polos trap racecar
# Reverse pairs are:
# made edam
# pools sloop
# part trap
# racecar racecar